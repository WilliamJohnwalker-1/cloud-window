/**
 * Look up the province for a given city name.
 * Returns the province name (e.g. "浙江省") or null if the city is not found.
 * Municipalities return their own province-level name (e.g. "北京" → "北京市").
 */
export function getProvinceForCity(cityName: string): string | null {
  return CITY_TO_PROVINCE[cityName] ?? null;
}

/**
 * Extract a deduplicated, sorted list of provinces from an array of cities.
 * Cities with no province mapping are silently excluded.
 */
export function getProvincesFromCities(cities: City[]): string[] {
  const provinceSet = new Set<string>();
  for (const city of cities) {
    const province = city.province || CITY_TO_PROVINCE[city.name] || null;
    if (province) {
      provinceSet.add(province);
    }
  }
  return Array.from(provinceSet).sort();
}

/**
 * Filter cities that belong to a given province.
 * Cities with no province mapping are excluded.
 */
export function getCitiesByProvince(cities: City[], province: string): City[] {
  return cities.filter((city) => {
    const cityProvince = city.province || CITY_TO_PROVINCE[city.name] || null;
    return cityProvince === province;
  });
}
